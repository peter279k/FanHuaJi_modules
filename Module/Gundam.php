    /**
     * input format is like the following:
     *
     *     大陸：菲利特；台灣：菲利特；香港：菲力特；當前顯示為：菲利特
     *     大陸：明日野；台灣：明日野；香港：亞斯諾；當前顯示為：明日野
     *     大陸：瑪麗娜；台灣：瑪莉娜；香港：瑪蓮娜；當前顯示為：瑪莉娜
     *     大陸：冬·博亞基；台灣：唐·博亞基；香港：當·寶亞治；當前顯示為：唐·博亞基
     *     ..............
     *
     * usage:
     *
     *     $this->generateMappingFromWikiPage($textFromWiki, 'hk', true);
     *
     * @return array [parsed data in array]
     */
    protected function generateMappingFromWikiPage (&$text, $targetLocale='tw', $varExport=false) {
        $conversions = $this->parseWikiPage($text);
        $mapping = $this->buildMapping($conversions, $targetLocale);
        if ($varExport) {
            foreach ($mapping as $from => &$to) {
                echo "'{$from}' => '{$to}',\n";
            }
        } else {
            return $mapping;
        }
    }

    protected function parseWikiPage (&$text) {
        // split by lines
        $lines = array_map('trim', explode("\n", $text));
        // sanitize
        $lines = preg_filter('/(简体|簡體|大陸|[台臺][湾灣]|香港)：/uS', '$0', $lines);
        // extract conversions into an array
        $conversions = [];
        foreach ($lines as &$line) {
            if (preg_match_all('/(简体|簡體|大陸|[台臺][湾灣]|香港)：([^；]+)/uS', $line, $matches, PREG_SET_ORDER)) {
                $conversion = [];
                foreach ($matches as &$match) {
                    list($whole, $locale, $name) = $match;
                    switch ($locale) {
                        default:
                            echo "WTF??? {$whole}\n";
                            break;
                        case '简体':
                        case '簡體':
                        case '大陸':
                            $conversion['cn'] = $name;
                            break;
                        case '台湾':
                        case '台灣':
                        case '臺灣':
                            $conversion['tw'] = $name;
                            break;
                        case '香港':
                            $conversion['hk'] = $name;
                            break;
                    }
                }
                $conversions[] = $conversion;
            }
        }
        return $conversions;
    }

    protected function buildMapping (array &$conversions, $targetLocale) {
        $nameDelimiters = '．·•';
        $textToRegex = function ($text, $type='from') use (&$nameDelimiters) {
            switch ($type) {
                default:
                case 'from':
                    $fixTable = [
                        "[{$nameDelimiters}]" => "([{$nameDelimiters}])",
                        '[雷鐳]' => '[雷鐳]',
                        '[裡里]' => '[裡里]',
                        '[贊讚]' => '[贊讚]',
                        '[菸煙]' => '[菸煙]',
                        '哥[德特]' => '哥[德特]',
                        '大將' => '大將(?![軍])',
                        '大魔' => '大魔(?![王])',
                    ];
                    break;
                case 'to':
                    $fixTable = [
                        "[{$nameDelimiters}]" => '\$1',
                    ];
                    break;
            }
            foreach ($fixTable as $sr => &$rep) {
                $text = preg_replace("/{$sr}/iumS", $rep, $text);
            }
            return $text;
        };
        $ret = [];
        foreach ($conversions as &$conversion) {
            $cn = isset($conversion['cn']) ? $conversion['cn'] : '';
            $hk = isset($conversion['hk']) ? $conversion['hk'] : '';
            $tw = isset($conversion['tw']) ? $conversion['tw'] : '';
            switch ($targetLocale) {
                case 'cn': $froms = [$tw, $hk]; $to = $cn; break;
                case 'hk': $froms = [$cn, $tw]; $to = $hk; break;
                case 'tw': $froms = [$cn, $hk]; $to = $tw; break;
                default: die("WTF??? #1");
            }
            // clean empty
            $froms = array_filter($froms);
            // skip
            if (empty($froms) || empty($to)) continue;
            //
            $keys = [];
            foreach ($froms as &$from) {
                if (
                    $from != $to &&
                    mb_strlen($from, 'UTF-8') > 1
                ) {
                    $keys[$from] = true;
                }
            }
            foreach ($keys as $key => &$_) {
                $key = $textToRegex($key, 'from');
                $to = $textToRegex($to, 'to');
                $ret[$key] = $to;
            }
        }
        return $ret;
    }

